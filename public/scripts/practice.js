class Product {

    constructor(id, name, price) {
        this.productId = id
        this.productName = name
        this.productPrice = price 
    }

    getProductName() {
        return this.productName
    }

    setProductName(price) {
        return this.productName = price
    }

    getProductPrice() {
        return this.productPrice
    }

    setProductPrice(price) {
        this.productPrice = price
    }

}




